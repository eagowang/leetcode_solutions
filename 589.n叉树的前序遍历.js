/**
 * // Definition for a Node.
 * function Node(val,children) {
 *    this.val = val;
 *    this.children = children;
 * };
 */
/**
 * @param {Node} root
 * @return {number[]}
 */
var preorder = function(root) {
  var stack = [root];
  var result = [];
  var node = stack.shift();
  result.push(node.value);
  while (stack.length > 0) {}
};
